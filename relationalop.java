public class relationalop
{
     public static void main(String[] args)
    {
        int a=5, b=12;
        System.out.println("RELATIONAL OPERATORS");
        System.out.println(a<b);
        System.out.println(a>b);
        System.out.println(a==b);
        System.out.println(a>=b);
        System.out.println(a<=b);
        System.out.println(a!=b);
    }
}
